    private TraSua getDuLieuTuForm() {
        TraSua nd = new TraSua();
        //Lấy giá trị từ txtUser.getText gan cho thuộc tính UserName
        nd.setMon(txtMon.getText().trim());
        nd.setSoLuong(Integer.parseInt(tfSoLuong.getText()));
        nd.setGhiChu(tfGhiChu.getText().trim());
        //C1:
        if (rdoTranChauDen.isSelected()) {
            nd.setTopping(1);
        } else if (rdoThach.isSelected()) {
            nd.setTopping(2);
        }else if (rdoTranChauTrang.isSelected()) {
            nd.setTopping(3);
        } else {
            nd.setTopping(0);
        }
        //C2:
        
        nd.setTrangThai(cbTrangThai.isSelected());
        
        return nd;
    }
