    private void fillToTable() {
        listND = service.getListNguoiDung(); //lấy ds người dùng từ service
        //đổi dữ liệu từ dòng đầu tiên
        defaultTable.setRowCount(0);
        for (TraSua nd : listND) {
            defaultTable.addRow(new Object[]{nd.getMon(), nd.getSoLuong(), nd.getToping() == 1 ? "Tran chau den" : "Thach", nd.getGhiChu(), nd.isTrangThai()});
        }
    }
    private void fillToForm(int rowIndex) {
        //lay ra doi tuonng nguoi dung tai rfowindex
        TraSua nd = listND.get(rowIndex);
        //do thuoc tinh username 
        txtMon.setText(nd.getMon());
        tfSoLuong.setText(String.valueOf(nd.getSoLuong()));
      
        //chojn user khi quyen bawng 1
        if (nd.getToping() == 1) {
            rdoTranChauDen.setSelected(true);
        } else {
            rdoThach.setSelected(true);
        }
        tfGhiChu.setText(nd.getGhiChu());
        //do trang thai len ChkTrangThai
        cbTrangThai.setSelected(nd.isTrangThai());
        
    }
    
