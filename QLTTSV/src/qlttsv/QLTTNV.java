    public void fillTable(){
        tableModel.setRowCount(0);// xoa tat ca cac hang trong bang 
    for(NhanVien item:list){
        tableModel.addRow(new Object[]{item.getHoten(),item.getNgaysinh(),item.getGioitinh(),item.getCmtnd(),item.getDiachi(),item.getHonnhan(),item.getHocvan(),item.getBacluong(),item.isTrangthai()});
        tableModel.fireTableDataChanged();
        if (item.getGioitinh().equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
    }
    
    }
