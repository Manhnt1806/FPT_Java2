    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtHoten.getText().equals("")){
            sb.append("Ten sinh viên khôg được để trống \n");   
        }
        if(txtCMTND.getText().equals("")){
            sb.append("CMTND không được để trống \n");  
        }
        if(txtBacluong.getText().equals("")){
            sb.append("Bac luong không được để trống va phai la so \n");  
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        NhanVien nv =new NhanVien();
        nv.setHoten(txtHoten.getText());
        nv.setNgaysinh(txtNgaysinh.getText());
        nv.setGioitinh(txtNgaysinh.toString());
        nv.setCmtnd((txtCMTND.getText()));
        nv.setDiachi(txtDiachi.getText());
        nv.setHonnhan(cbHonNhan.getSelectedItem().toString());
        nv.setHocvan(cbHocvan.getSelectedItem().toString());
        nv.setBacluong(Double.parseDouble(txtBacluong.getText()));
        nv.setTrangthai(cbxTrangthai.isSelected());
        list.add(nv);
        fillTable();

    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int selectedRow= tblNhanVien.getSelectedRow();
        if(selectedRow>=0){
            String nv=(String) tblNhanVien.getValueAt(selectedRow, 3);
            for (NhanVien item : list) {
                if(nv.equals(item.getCmtnd())){
                    txtCMTND.setText(item.getCmtnd());
                    txtHoten.setText(item.getHoten());
                    txtDiachi.setText(item.getDiachi());
                    txtNgaysinh.setText(item.getNgaysinh());
                    cbHocvan.setSelectedItem(item.getHocvan());
                    txtBacluong.setText(""+item.getBacluong());
                    cbHonNhan.setSelectedItem(item.getHonnhan());
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

