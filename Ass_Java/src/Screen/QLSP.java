    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        if(tfMa.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Vui long nhap ma san pham!");
        }else if(tfTen.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Vui long nhap ten san pham!");
        }else{
            SanPham maSp = service.search(tfMa.getText().trim());
            if(maSp!=null){    
                JOptionPane.showMessageDialog(this, "Đã tìm thấy!");
                SanPham sp = getDuLieuForm();
                service3.add(sp);
                fillToTable3();
            }else{
                JOptionPane.showMessageDialog(this, "Khong tim thay san pham!");
            }
        }
        
    }//GEN-LAST:event_btnTimActionPerformed

