    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int rowIndex = tblQLSP.getSelectedRow();
        if(rowIndex>=0){
            int confirm = JOptionPane.showConfirmDialog(this, "Ban co chac muon xoa khong?");
            if(confirm == 0){
                service.delete(rowIndex);
                fillToTable();
            }
        }else {
            JOptionPane.showMessageDialog(this, "Phai chon doi tuong xoa");
        }
        service.saveToFile();
    }//GEN-LAST:event_btnXoaActionPerformed
    private boolean validateSP(){
        if(tfMa.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ma san pham khong duoc de trong!");
            return false;
        }else if(tfTen.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ten san pham khong duoc de trong!");
            return false;
        }else if(tfGia.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Gia san pham khong duoc de trong!");
            return false;
        }
        try {
                Double gia = Double.parseDouble(tfGia.getText().trim());
                if (gia <= 0) {
                    JOptionPane.showMessageDialog(this,"Gia phai la so duong!");
                    return false;
                }
            } catch (NumberFormatException e) {
                 JOptionPane.showMessageDialog(this,"Gia phai la so!");
            }
        return true;
    }
    private void fillToTable(){
        listSP = service.getListSanPham(); 
        defaultTable.setRowCount(0);
        for (SanPham sp : listSP) {
            defaultTable.addRow(new Object[]{sp.getMa(), sp.getTenSP(), sp.getHangSP(), 
                sp.getMauSP(),sp.getGiaSP(), sp.isTrangThaiSP()});
        }
    }
    private void fillToTable3(){
        listS = service3.getListS(); 
        defaultTable3.setRowCount(0);
        for (SanPham sp: listS) {
            defaultTable3.addRow(new Object[]{sp.getMa(), sp.getTenSP(), 
                sp.getHangSP(), sp.getGiaSP(), sp.isTrangThaiSP()});
        }
    }
    private void fillToForm(int rowIndex) {
        SanPham sp = listSP.get(rowIndex);
        tfMa.setText(sp.getMa());
        tfTen.setText(sp.getTenSP());
        cbHang.setSelectedItem(sp.getHangSP());
        tfMau.setText(sp.getMauSP());
        tfGia.setText(String.valueOf(sp.getGiaSP()));
        if (sp.getLoaiSP() == 1) {
            rdoCu.setSelected(true);
        } else {
            rdoMoi.setSelected(true);
        }
        chkTrangThai.setSelected(sp.isTrangThaiSP());  
    }
