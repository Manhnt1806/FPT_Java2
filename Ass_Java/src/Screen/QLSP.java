    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
    if(validateSP()){
        int rowIndex = tblQLSP.getSelectedRow();
        if(rowIndex>=0){
            SanPham SanPham = getDuLieuForm();
        service.update(rowIndex, SanPham);
        fillToTable();
        }else{
            JOptionPane.showMessageDialog(this, "Phai chon doi tuong sua");
        }  
    }
    service.saveToFile();
    }//GEN-LAST:event_btnSuaMouseClicked

